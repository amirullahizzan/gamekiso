#include "Main.h"
#include "Game.h"
#include "Random.h"
#include "Npc.h"

//---------------------------------------------------------------------------------
//	初期化処理
//---------------------------------------------------------------------------------
void Npc::Init( int image )
{
	m_image = image;
	//	ＮＰＣの半径の設定
	m_radius = 22.5f;
	//	座標をステージに入るようなランダムに設定
	m_pos.x = GetRandomF(STAGE_LEFT +m_radius, SCREEN_W - m_radius );
	m_pos.y = GetRandomF( m_radius, STAGE_H - m_radius );
	m_rot = GetRandomF( 360.0f );
}
//---------------------------------------------------------------------------------
//	更新処理
//---------------------------------------------------------------------------------
void Npc::Update()
{
	m_rot += 1.0f;
}
//---------------------------------------------------------------------------------
//	描画処理
//---------------------------------------------------------------------------------
void Npc::Render()
{
	DrawRotaGraphF( m_pos.x, m_pos.y, 1.0f, TO_RADIAN( m_rot ), m_image, TRUE );
}
//---------------------------------------------------------------------------------
//	終了処理
//---------------------------------------------------------------------------------
void Npc::Exit()
{
}
