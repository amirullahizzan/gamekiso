#include "Main.h"
#include "Game.h"
#include "Title.h"

//=================================================================================
//	タイトル画面の処理
//=================================================================================

int title_image;
int name_image;

int name_y;

//---------------------------------------------------------------------------------
//	データの読み込み
//---------------------------------------------------------------------------------
void TitleLoad()
{
	title_image = LoadGraph( "data/title_bg.bmp" );
	name_image = LoadGraph( "data/title_name.bmp" );
}
//---------------------------------------------------------------------------------
//	初期化
//---------------------------------------------------------------------------------
void TitleInit()
{
	//初期化
	name_y = -100;
}
//---------------------------------------------------------------------------------
//	更新処理
//---------------------------------------------------------------------------------
void TitleUpdate()
{
	name_y += 3;
	if( name_y > 150 ){
		name_y = 150;
	}

	if( PushHitKey( KEY_INPUT_SPACE ) ){
		scene_next = SCENE_STAGE;
	}
}
//---------------------------------------------------------------------------------
//	描画処理
//---------------------------------------------------------------------------------
void TitleRender()
{
	DrawGraph( 0, 0, title_image, TRUE );
	DrawRotaGraph( SCREEN_W / 2, name_y, 1.0f, 0.0f, name_image, TRUE );

	DrawString( 20, 20, "【タイトル画面】", GetColor( 255, 255, 255 ) );
	DrawString( 20, 40, "スペースキーでステージへ", GetColor( 255, 255, 255 ) );
}
//---------------------------------------------------------------------------------
//	終了処理
//---------------------------------------------------------------------------------
void TitleExit()
{
	DeleteGraph( title_image );
}
