#include "Main.h"
#include "Float2.h"

//---------------------------------------------------------------------------------
//	コンストラクタ
//---------------------------------------------------------------------------------
Float2::Float2(float _x, float _y):x(_x), y(_y)
{

}
//---------------------------------------------------------------------------------
//	ゼロ初期化
//---------------------------------------------------------------------------------
void Float2::clear()
{
	x = 0.0f;
	y = 0.0f;
}
//---------------------------------------------------------------------------------
//	値のセット
//---------------------------------------------------------------------------------
void Float2::set( float x, float y )
{
	this->x = x;
	this->y = y;
}
//---------------------------------------------------------------------------------
//	値のセット
//---------------------------------------------------------------------------------
void Float2::set( Float2 v )
{
	x = v.x;
	y = v.y;
}

//---------------------------------------------------------------------------------
//	２つの Float2 の距離を求める
//---------------------------------------------------------------------------------
float GetFloat2Distance( Float2 pos1, Float2 pos2 )
{
	float x = pos1.x - pos2.x;
	float y = pos1.y - pos2.y;
	float distance = sqrtf( x * x + y * y );
	return distance;
}
