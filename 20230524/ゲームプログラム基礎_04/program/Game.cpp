#include "Main.h"
#include "Game.h"


int pos_x[10];
int pos_y[10];



//---------------------------------------------------------------------------------
//	初期化処理
//---------------------------------------------------------------------------------

void GameInit()
{
	
	for (int i = 0; i < 10;i++)
	{
		pos_x[i] = GetRand(SCREEN_W);
		pos_y[i] = GetRand(SCREEN_H);
	}
	
	
	

}
//---------------------------------------------------------------------------------
//	更新処理
//---------------------------------------------------------------------------------
void GameUpdate()
{
	
	if( PushHitKey( KEY_INPUT_SPACE ) )
	{
		for(int i = 0; i < 10; i++)
		{
			pos_x[i] = GetRand(SCREEN_W);
			pos_y[i] = GetRand(SCREEN_H);
		}
	}
	
}
//---------------------------------------------------------------------------------
//	描画処理
//---------------------------------------------------------------------------------

void GameRender()
{
	for (int i = 0; i < 10; i++)
	{
	DrawCircle(pos_x[i], pos_y[i], 10, GetColor(255, 255, 0));
	}
	
	
	
	DrawString( 20, 20, "複数の円の描画", GetColor( 255, 255, 255 ) );
	DrawString( 20, 40, "スペースキー：座標を設定しなおす（ランダムに）", GetColor( 255, 255, 255 ) );
	

}
//---------------------------------------------------------------------------------
//	終了処理
//---------------------------------------------------------------------------------
void GameExit()
{
}
