void Npc::Init( int image )
{
	m_image = image;

	m_radius = 22.5f;

	m_pos.x = GetRandomF( STAGE_W - m_radius)+ m_radius;
	m_pos.y = GetRandomF(STAGE_H - m_radius) + m_radius;
	m_rot = GetRandomF( 360.0f );
}
//---------------------------------------------------------------------------------
//	XVˆ—
//---------------------------------------------------------------------------------
void Npc::Update()
{
	m_rot += 1.0f;
}
